'''
Escreva um programa que solicite ao usuário a largura e a altura de um retângulo e calcule sua área. Exiba a área calculada.
'''

largura = float(input('digite a largura do retângulo: '))
altura = float(input('digite a altura do retângulo: '))
area = largura * altura

print(f'A área do seu rentângulo é de {area} metros quadrados.')
